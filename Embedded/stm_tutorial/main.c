/**
* @brief This tasks waits for events from the message queue and 
        calls the state machine with this events. 
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask02 */
void StartTask02(void *argument)
{
  /* USER CODE BEGIN StartTask02 */
  /* Infinite loop */
 
  uint16_t msg=SM_NO_MSG;

  sm(&smInst, msg); // call once with SM_NO_MSG to initialize the state machine

  for(;;)
  {
    osStatus_t status = osMessageQueueGet(queue0Handle, &msg, NULL, osWaitForever);

#ifdef __DEBUG__
    // Log new receveid event new message from queue
    char* dbgText;
    dbgText = smGetNameByEvent(msg);
    dbgText[strlen(dbgText)]='\n';
    // send debug text to USB port realizing a serial port
    uint8_t res = CDC_Transmit_FS((uint8_t*)dbgText, strlen(dbgText));
#endif

    // call state machine with new message from queue
    sm(&smInst, msg);
  }
  /* USER CODE END StartTask02 */
}

